import turtle
win = turtle.Screen()
tic = turtle.Turtle()
tic.speed(10)
win.setworldcoordinates(-0.5,-0.5,3.5,3.5)

for i in range (0,10):
    tic.penup()
    tic.goto(0,i)
    tic.pendown()
    tic.forward(9)

tic.left(90)
for i in range (0,10):
    tic.penup()
    tic.goto(0,i)
    tic.pendown()
    tic.forward(9)

    
